export const listenToDailyReports = (callback: (reports: DailyReportData[]) => void): (() => void) => {
  if (isFirebaseConfigured) {
    const q = query(collection(db, 'dailyReports'), orderBy('date', 'desc'), limit(7));
    return onSnapshot(q, (snapshot) => {
      const reportsList: DailyReportData[] = [];
      snapshot.forEach((doc) => {
        reportsList.push(doc.data() as DailyReportData);
      });
      // Sort ascending for chart rendering
      reportsList.sort((a, b) => a.date.localeCompare(b.date));
      callback(reportsList);
    });
  } else {
    const trigger = () => {
      const reports = getLocalCollection<DailyReportData>(MOCK_DAILY_REPORTS_KEY);
      // Sort ascending for charts
      reports.sort((a, b) => a.date.localeCompare(b.date));
      callback(reports);
    };
    trigger();
    window.addEventListener('storage', trigger);
    return () => window.removeEventListener('storage', trigger);
  }
};

// Synchronize daily reports when adding expenses or purchases
export const addExpenseToFirestore = async (amount: number, dateStr: string): Promise<void> => {
  if (isFirebaseConfigured) {
    const reportRef = doc(db, 'dailyReports', dateStr);
    const reportSnap = await getDoc(reportRef);
    if (reportSnap.exists()) {
      const data = reportSnap.data() as DailyReportData;
      const newExpenses = (data.expenses || 0) + amount;
      await updateDoc(reportRef, {
        expenses: newExpenses,
        profit: (data.revenue || 0) - newExpenses
      });
    } else {
      await setDoc(reportRef, {
        date: dateStr,
        revenue: 0,
        expenses: amount,
        profit: -amount,
        invoices: 0
      });
    }
  } else {
    updateMockDailyReports(0, amount, dateStr);
  }
};

const updateMockDailyReports = (revenue: number, expenses: number, dateStr: string) => {
  const reports = getLocalCollection<DailyReportData>(MOCK_DAILY_REPORTS_KEY);
  const existing = reports.find(r => r.date === dateStr);
  
  if (existing) {
    existing.revenue += revenue;
    existing.expenses += expenses;
    existing.profit = existing.revenue - existing.expenses;
    if (revenue > 0) existing.invoices += 1;
  } else {
    reports.push({
      date: dateStr,
      revenue,
      expenses,
      profit: revenue - expenses,
      invoices: revenue > 0 ? 1 : 0
    });
  }
  
  // Keep last 7 days only or sort
  reports.sort((a, b) => a.date.localeCompare(b.date));
  setLocalCollection(MOCK_DAILY_REPORTS_KEY, reports);
};

const recordInvoiceInDailyReports = async (amount: number, paidAmount: number, expenseAmount: number): Promise<void> => {
  const dateStr = new Date().toISOString().split('T')[0];
  const reportRef = doc(db, 'dailyReports', dateStr);
  const reportSnap = await getDoc(reportRef);

  if (reportSnap.exists()) {
    const data = reportSnap.data() as DailyReportData;
    await updateDoc(reportRef, {
      revenue: (data.revenue || 0) + amount,
      expenses: (data.expenses || 0) + expenseAmount,
      profit: ((data.revenue || 0) + amount) - ((data.expenses || 0) + expenseAmount),
      invoices: (data.invoices || 0) + 1
    });
  } else {
    await setDoc(reportRef, {
      date: dateStr,
      revenue: amount,
      expenses: expenseAmount,
      profit: amount - expenseAmount,
      invoices: 1
    });
  }
};

// Sync whole database helper for initial transition
export const syncInvoicesToFirestore = async (invoices: any[]): Promise<void> => {
  for (const inv of invoices) {
    const billData: Omit<Bill, 'id'> = {
      customerName: inv.customerName,
      amount: inv.totalAmount,
      status: inv.paymentStatus,
      date: inv.createdAt ? inv.createdAt.split('T')[0] : new Date().toISOString().split('T')[0],
      createdBy: inv.createdBy || 'System',
      invoiceNumber: inv.invoiceNumber
    };
    await addBillToFirestore(billData, inv.id);
  }
};

