export const loginUser = async (email: string, password: string): Promise<UserProfile> => {
  if (isFirebaseConfigured) {
    // 1. Authenticate with Firebase Auth
    // Fallback simple username/email helper for standard demo logins in Firebase
    let targetEmail = email;
    if (!email.includes('@')) {
      targetEmail = email === 'superadmin' ? 'superadmin@photobill.com' : 'admin@photobill.com';
    }
    
    const userCredential = await signInWithEmailAndPassword(auth, targetEmail, password);
    const uid = userCredential.user.uid;
    
    // 2. Fetch User Role from Firestore
    const userDocRef = doc(db, 'users', uid);
    const userDoc = await getDoc(userDocRef);
    
    if (userDoc.exists()) {
      return { uid, ...userDoc.data() } as UserProfile;
    } else {
      // Create user profile on first login with default role based on email if not exists
      const isSuper = targetEmail.includes('superadmin');
      const role = isSuper ? 'superadmin' : 'admin';
      const profile: UserProfile = {
        uid,
        name: isSuper ? 'Super Admin' : 'Billing Manager',
        email: targetEmail,
        role,
        createdAt: new Date().toISOString()
      };
      await setDoc(userDocRef, profile);
      return profile;
    }
  } else {
    // Local Mock Logic
    let cleanEmail = email.trim();
    if (!cleanEmail.includes('@')) {
      cleanEmail = cleanEmail === 'superadmin' ? 'superadmin@photobill.com' : 'admin@photobill.com';
    }

    const users = getLocalCollection<UserProfile>(MOCK_USERS_KEY);
    const foundUser = users.find(u => u.email.toLowerCase() === cleanEmail.toLowerCase());

    if (foundUser) {
      // In mock, let password "123456" or any admin password succeed
      if (password === '123456' || password === 'super123' || password === 'admin123') {
        return foundUser;
      }
    }
    throw new Error('Invalid email/username or password. Try superadmin@photobill.com / 123456');
  }
};

