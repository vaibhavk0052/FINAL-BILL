const mockEmployeeListeners: ((employees: Employee[]) => void)[] = [];

export const getEmployees = async (callback: (employees: Employee[]) => void): Promise<() => void> => {
  if (isFirebaseConfigured) {
    const q = collection(db, 'employees');
    return onSnapshot(q, (snapshot) => {
      const emps: Employee[] = [];
      snapshot.forEach((doc) => {
        emps.push({ id: doc.id, ...doc.data() } as Employee);
      });
      callback(emps);
    });
  } else {
    mockEmployeeListeners.push(callback);
    // Initial fetch
    const emps = getLocalCollection<Employee>(MOCK_EMPLOYEES_KEY);
    callback(emps);
    return () => {
      const idx = mockEmployeeListeners.indexOf(callback);
      if (idx !== -1) {
        mockEmployeeListeners.splice(idx, 1);
      }
    };
  }
};

export const addEmployee = async (employeeData: Omit<Employee, 'id'>, authPassword?: string): Promise<void> => {
  if (isFirebaseConfigured) {
    // To register employee in Firebase Auth without logging out current Super Admin:
    // Initialize a temporary secondary app
    const secondaryAppName = 'SecondaryAuthRegister_' + Date.now();
    const secApp = initializeApp(firebaseConfig, secondaryAppName);
    const secAuth = getAuth(secApp);
    
    try {
      // 1. Create user in Firebase Auth
      const userCredential = await createUserWithEmailAndPassword(
        secAuth, 
        employeeData.email, 
        authPassword || '123456'
      );
      const uid = userCredential.user.uid;

      // Map role to standard user privilege roles for login
      let mappedRole: 'superadmin' | 'admin' | 'staff' = 'staff';
      const roleLower = employeeData.role.toLowerCase();
      if (roleLower.includes('super')) {
        mappedRole = 'superadmin';
      } else if (roleLower.includes('admin') || roleLower.includes('manager')) {
        mappedRole = 'admin';
      }

      // 2. Save in Firestore users collection
      const newProfile: UserProfile = {
        uid,
        name: employeeData.name,
        email: employeeData.email,
        role: mappedRole,
        createdAt: new Date().toISOString()
      };
      await setDoc(doc(db, 'users', uid), newProfile);

      // 3. Save in Firestore employees collection
      const newEmployee: Employee = {
        id: uid,
        name: employeeData.name,
        email: employeeData.email,
        role: employeeData.role,
        phone: employeeData.phone,
        salary: employeeData.salary,
        status: employeeData.status
      };
      await setDoc(doc(db, 'employees', uid), newEmployee);
    } finally {
      // Delete secondary app instance to clean up
      await deleteApp(secApp);
    }
  } else {
    // Local Mock Logic
    const employees = getLocalCollection<Employee>(MOCK_EMPLOYEES_KEY);
    const newId = 'emp-' + Math.random().toString(36).substr(2, 9);
    const newEmployee: Employee = {
      id: newId,
      ...employeeData
    };
    employees.push(newEmployee);
    setLocalCollection(MOCK_EMPLOYEES_KEY, employees);

    // Map role to standard user privilege roles for login
    let mappedRole: 'superadmin' | 'admin' | 'staff' = 'staff';
    const roleLower = employeeData.role.toLowerCase();
    if (roleLower.includes('super')) {
      mappedRole = 'superadmin';
    } else if (roleLower.includes('admin') || roleLower.includes('manager')) {
      mappedRole = 'admin';
    }

    // Also register mock user login
    const users = getLocalCollection<UserProfile>(MOCK_USERS_KEY);
    users.push({
      uid: newId,
      name: employeeData.name,
      email: employeeData.email,
      role: mappedRole,
      createdAt: new Date().toISOString()
    });
    setLocalCollection(MOCK_USERS_KEY, users);

    // Notify all mock subscription listeners immediately
    mockEmployeeListeners.forEach((cb) => cb(employees));
  }
};

