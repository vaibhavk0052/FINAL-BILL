export const listenToBills = (callback: (bills: Bill[]) => void): (() => void) => {
  if (isFirebaseConfigured) {
    const q = collection(db, 'bills');
    return onSnapshot(q, (snapshot) => {
      const billsList: Bill[] = [];
      snapshot.forEach((doc) => {
        billsList.push({ id: doc.id, ...doc.data() } as Bill);
      });
      callback(billsList);
    });
  } else {
    // Fallback mock listener
    const trigger = () => {
      const bills = getLocalCollection<Bill>(MOCK_BILLS_KEY);
      callback(bills);
    };
    trigger();
    window.addEventListener('storage', trigger);
    return () => window.removeEventListener('storage', trigger);
  }
};

export const addBillToFirestore = async (billData: Omit<Bill, 'id'>, customId?: string): Promise<void> => {
  const id = customId || 'bill-' + Math.random().toString(36).substr(2, 9);
  if (isFirebaseConfigured) {
    const billDocRef = doc(db, 'bills', id);
    const docSnap = await getDoc(billDocRef);
    const isNew = !docSnap.exists();
    
    await setDoc(billDocRef, billData);
    
    if (isNew) {
      // Auto-update daily reports metrics
      await recordInvoiceInDailyReports(billData.amount, billData.status === 'completed' ? billData.amount : 0, 0);
    }
  } else {
    const bills = getLocalCollection<Bill>(MOCK_BILLS_KEY);
    const existingIndex = bills.findIndex(b => b.id === id);
    const isNew = existingIndex === -1;
    if (!isNew) {
      bills[existingIndex] = { id, ...billData };
    } else {
      bills.push({ id, ...billData });
    }
    setLocalCollection(MOCK_BILLS_KEY, bills);

    if (isNew) {
      // Update mock daily reports
      updateMockDailyReports(billData.amount, 0, billData.date);
    }
  }
};

export const deleteBillFromFirestore = async (id: string): Promise<void> => {
  if (isFirebaseConfigured) {
    try {
      await deleteDoc(doc(db, 'bills', id));
    } catch (err) {
      console.error("Error deleting bill from Firestore:", err);
    }
  } else {
    const bills = getLocalCollection<Bill>(MOCK_BILLS_KEY);
    const filtered = bills.filter(b => b.id !== id);
    setLocalCollection(MOCK_BILLS_KEY, filtered);
  }
};

